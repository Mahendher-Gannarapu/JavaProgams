package September_2024.Sept_16092024;

public class CompoundAssignmentOp {
    public static void main(String[] args) {
        //  Compound Assignment Operator += ,-= , *=, /=
        int a=50;
        System.out.println(a+=20);
        System.out.println(a-=20);
        System.out.println(a*=20);
        System.out.println(a/=20);

        int b=40;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
    }
}
