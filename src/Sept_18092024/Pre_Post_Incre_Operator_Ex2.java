package Sept_18092024;

public class Pre_Post_Incre_Operator_Ex2 {
    public static void main(String[] args) {
        byte b = 10;
        b++;
        System.out.println(b);
        ++b;
        System.out.println(b);

        int c = 10;
        c--;
        System.out.println(c);
        --c;
        System.out.println(c);

        /* output
        11
        12
        9
        8
         */
    }
}
