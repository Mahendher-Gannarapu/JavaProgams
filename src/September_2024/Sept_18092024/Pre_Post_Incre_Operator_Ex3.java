package September_2024.Sept_18092024;

public class Pre_Post_Incre_Operator_Ex3 {
    public static void main(String[] args) {
        byte b = 101;
        ++b;
        System.out.println(b);
        b++;
        System.out.println(b);


        int c = 109;
        --c;
        System.out.println(c);
        c--;
        System.out.println(c);
    }
}

