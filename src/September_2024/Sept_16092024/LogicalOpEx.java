package September_2024.Sept_16092024;

public class LogicalOpEx {
    public static void main(String[] args) {
        // !, && , || Logical operators

        boolean a= true;
        boolean b= false;

        System.out.println(a);
        System.out.println(b);

        System.out.println(!a);
        System.out.println(!b);

        System.out.println(a && b);
        System.out.println(a || b);

        }
}
