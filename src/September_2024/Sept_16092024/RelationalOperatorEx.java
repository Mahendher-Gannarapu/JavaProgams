package September_2024.Sept_16092024;

public class RelationalOperatorEx {
    public static void main(String[] args) {
        // Relational Operators  = boolean
        //  > ,< , >= ,<= , != , ==

        int a=1;
        int b=2;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
    }
}
